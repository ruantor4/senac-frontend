// IMPORTE DO COMPONENTE JFrame DO MODULO swing DA BIBLIOTECA javax.
import javax.swing.JFrame;

// DECLARAÇÃO DA CLASSE RadioButtonTeste
public class RadioButtonTest  
{
   // DECLARAÇÃO DO METODO "main"
   public static void main(String[] args)
   {
      RadioButtonFrame radioButtonFrame = new RadioButtonFrame(); // INSTANCIAÇÃO DA VARIAVEL radioButtonFrame do tipo RadioButtonFrame
      radioButtonFrame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE); // DEFININDO CONFIGURAÇÃO DA JANELA DO BOTÃO FECHAR, PARA FECHAR E FINALIZAR SISTEMA
      radioButtonFrame.setSize(300, 100); // DEFININDO O TAMANHO DA JANELA EM PIXEL
      radioButtonFrame.setVisible(true);  // DIFININDO VISIBILIDADE
   } 
   // FIM DO METODO "main"
} // FIM DA CLASS RadioButtonTest 

